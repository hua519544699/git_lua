--上下滑动函数
--参数x1,y1第一个点，x2，y2位第二个点，speed为滑动速度
--该函数旨在将滑动距离拆解,达到滑动更精确的目的
function myMove_UD(x1, y1, x2, y2,speed)
    local step = speed
    if y2 > y1 then step = -speed end 
    local cnt = math.abs((y2 - y1) /speed)
    if cnt == 0 then
        cnt = 1
        step = math.abs(y2 - y1)
    end
    touchDown(0, x1, y1)
    mSleep(50)
    for i = 1, cnt do
        mSleep(50)
        y1 = y1 - step
        touchMove(0, x1, y1)
    end
    touchUp(0)
    mSleep(100)
end
